class DiceLoss(nn.Module):
    def __init__(self, class_num=3, smooth=1):
        super(DiceLoss, self).__init__()
        self.smooth = smooth
        self.class_num = class_num

    def forward(self, input, target):
        input = torch.exp(input)
        self.smooth = 0.
        Dice = Variable(torch.Tensor([0]).float()).cuda()
        for i in range(1, self.class_num):
            input_i = input[:, i, :, :]
            target_i = (target == i).float()
            intersect = (input_i * target_i).sum()
            union = torch.sum(input_i) + torch.sum(target_i)
            if target_i.sum() == 0:
                dice = Variable(torch.Tensor([1]).float()).cuda()
            else:
                dice = (2 * intersect + self.smooth) / (union + self.smooth)
            Dice += dice
        dice_loss = 1 - Dice / (self.class_num - 1)
        return dice_loss

def target_seg2target_cls(labels_batch):
    # labels_batch 是一个形状为 (batch_size, height, width) 的张量

    batch_size, _, _ = labels_batch.shape

    cls_labels_batch = np.zeros(shape=(batch_size, 3), dtype=float)

    for batch_idx in range(batch_size):
        label_set = np.unique(labels_batch[batch_idx])

        for i in label_set:
            if i != 0:
                cls_labels_batch[batch_idx, i - 1] += 1

    cls_labels_batch = torch.from_numpy(cls_labels_batch).float()
    

    return cls_labels_batch


